package looping;

public class CountedLoop {
    public static void main(String[] args) {
        // For Loop
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        // For Each
        int num[] = {3, 1, 42, 24, 12};
        for (int x : num) {
            System.out.println(x);
        }        
    }   
}
